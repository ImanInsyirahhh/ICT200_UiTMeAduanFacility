CREATE DATABASE EADUAN;
USE EADUAN;

CREATE TABLE COLLEGE(
COLLEGE_CODE CHAR(3) NOT NULL,
COLLEGE_TYPE VARCHAR(3),
COLLEGE_NAME VARCHAR(25),
PRIMARY KEY (COLLEGE_CODE)
);

CREATE TABLE STAFF(
STAFF_ID VARCHAR(5) NOT NULL,
STAFF_NAME VARCHAR(35),
STAFF_PHONE VARCHAR(11),
STAFF_POSITION VARCHAR(25),
COLLEGE_CODE CHAR(3),
PRIMARY KEY (STAFF_ID),
FOREIGN KEY (COLLEGE_CODE) REFERENCES COLLEGE (COLLEGE_CODE)
);

CREATE TABLE ROOM(
ROOM_ID VARCHAR(10) NOT NULL,
ROOM_NUM INT,
ROOM_SIZE INT,
ROOM_PRICE DECIMAL(5,2),
COLLEGE_CODE CHAR(3),
STAFF_ID VARCHAR(5),
PRIMARY KEY (ROOM_ID),
FOREIGN KEY (COLLEGE_CODE) REFERENCES COLLEGE (COLLEGE_CODE),
FOREIGN KEY (STAFF_ID) REFERENCES STAFF (STAFF_ID)
);


CREATE TABLE STUDENT(
STU_ID VARCHAR(10) NOT NULL,
STU_NAME VARCHAR(35),
STU_PHONE VARCHAR(11),
STU_DOC DATE,
ROOM_ID VARCHAR(10),
STAFF_ID VARCHAR(10),
PRIMARY KEY (STU_ID),
FOREIGN KEY (ROOM_ID) REFERENCES ROOM (ROOM_ID),
FOREIGN KEY (STAFF_ID) REFERENCES STAFF (STAFF_ID)
);


CREATE TABLE COMPLAINT(
COMPLAINT_NO VARCHAR(10) NOT NULL,
COMPLAINT_DETAIL VARCHAR(35),
DOC DATE,
STU_ID VARCHAR(10),
STAFF_ID VARCHAR(5),
COLLEGE_CODE CHAR(3),
PRIMARY KEY(COMPLAINT_NO),
FOREIGN KEY (STU_ID) REFERENCES STUDENT (STU_ID),
FOREIGN KEY (STAFF_ID) REFERENCES STAFF (STAFF_ID),
FOREIGN KEY (COLLEGE_CODE) REFERENCES COLLEGE (COLLEGE_CODE)
);

